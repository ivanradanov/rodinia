// Compute (a*b) mod P
// See: https://code.google.com/p/crcutil/source/browse/code/gf_util.h
static inline uint64_t crc64_multiply_(uint64_t a, uint64_t b) {
  if ((a ^ (a-1)) < (b ^ (b-1))) {
    uint64_t t = a;
    a = b;
    b = t;
  }

  if (a == 0)
    return 0;

  uint64_t r = 0, h = UINT64_C(1) << 63;
  for (; a != 0; a <<= 1) {
    if (a & h) {
      r ^= b;
      a ^= h;
    }

    b = (b >> 1) ^ ((b & 1) ? crc64_poly : 0);
  }

  return r;
}

// Compute x**n mod P
static inline uint64_t crc64_x_pow_n_(uint64_t n) {
  uint64_t r = UINT64_C(1) << 63;
  for (size_t i = 0; n != 0; n >>= 1, ++i) {
    if (n & 1)
      r = crc64_multiply_(r, crc64_x_pow_2n[i]);
  }

  return r;
}

uint64_t crc64_combine(uint64_t cs1, uint64_t cs2, size_t nbytes2) {
  // For M = CONCAT(M1, M2) => CRC(M, a) = CRC(M2, CRC(M1, a)) and:
  // CRC(M, b) = CRC(M, a) + ((b-a)x^|M|) mod P.
  return cs2 ^ crc64_multiply_(cs1, crc64_x_pow_n_(8*nbytes2));
}

static const size_t crc64_min_thread_bytes = 1024;

__global__ void 
crc64_kernel(
  size_t *__restrict__ d_thread_sz, 
  uint64_t *__restrict__ d_thread_cs, 
  const unsigned char*__restrict__  d_data, 
  const uint64_t *__restrict__ d_crc64_table, 
  const uint64_t *__restrict__ d_crc64_interleaved_table, 
  size_t nbytes, int nthreads) 
{
  int tid = blockIdx.x * blockDim.x + threadIdx.x;
  size_t bpt = nbytes/nthreads;
  const unsigned char *start = d_data + bpt*tid;
  const unsigned char *end;
  if (tid != nthreads - 1)
    end = start + bpt;
  else
    end = d_data + nbytes;
    
  size_t sz = end - start;
  d_thread_sz[tid] = sz;
  d_thread_cs[tid] = crc64_device(start, sz, d_crc64_table, d_crc64_interleaved_table);
}

uint64_t crc64_parallel(const void *input, size_t nbytes) {

  if (nbytes > 2*crc64_min_thread_bytes) {
    int nthreads = 96*8*32; 

    if (nbytes < nthreads*crc64_min_thread_bytes)
      nthreads = nbytes/crc64_min_thread_bytes;

    uint64_t thread_cs[nthreads];
    size_t thread_sz[nthreads];

    const unsigned char *data = (const unsigned char*) input;

    uint64_t *d_thread_cs;
    uint64_t *d_crc64_table;
    uint64_t *d_crc64_interleaved_table;
    size_t *d_thread_sz;
    unsigned char *d_data;

    cudaMalloc((void**)&d_thread_sz, nthreads * sizeof(size_t));
    cudaMalloc((void**)&d_thread_cs, nthreads * sizeof(uint64_t));
    cudaMalloc((void**)&d_data, nbytes);
    cudaMalloc((void**)&d_crc64_table, 4*256*sizeof(uint64_t));
    cudaMalloc((void**)&d_crc64_interleaved_table, 4*256*sizeof(uint64_t));

    cudaMemcpy(d_data, data, nbytes , cudaMemcpyHostToDevice);
    cudaMemcpy(d_crc64_table, crc64_table, 4*256*sizeof(uint64_t) , cudaMemcpyHostToDevice);
    cudaMemcpy(d_crc64_interleaved_table, crc64_interleaved_table, 
		    4*256*sizeof(uint64_t) , cudaMemcpyHostToDevice);

    crc64_kernel<<<nthreads/64, 64>>>(d_thread_sz, d_thread_cs, d_data, 
		    d_crc64_table, d_crc64_interleaved_table, nbytes, nthreads);

    cudaMemcpy(thread_sz, d_thread_sz, nthreads * sizeof(size_t), cudaMemcpyDeviceToHost);
    cudaMemcpy(thread_cs, d_thread_cs, nthreads * sizeof(uint64_t), cudaMemcpyDeviceToHost);

    uint64_t cs = thread_cs[0];
    for (int i = 1; i < nthreads; ++i) {
      cs = crc64_combine(cs, thread_cs[i], thread_sz[i]);
    }
    cudaFree(d_thread_sz); 
    cudaFree(d_thread_cs);
    cudaFree(d_data);
    cudaFree(d_crc64_table);
    cudaFree(d_crc64_interleaved_table);
    return cs;
  }

  return crc64(input, nbytes);
}
